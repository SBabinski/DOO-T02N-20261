
public class Venda {

    int quantidade;
    double valorTotal;
    double valorDesconto;
    double valorFinal;

    public Venda(int quantidade, double valorTotal, double valorDesconto, double valorFinal){
        this.quantidade = quantidade;
        this.valorTotal = valorTotal;
        this.valorDesconto = valorDesconto;
        this.valorFinal = valorFinal;
    }

}
